public class Main {
    public static void main(String[] args) {

        int i = 10;
        System.out.println("i = " + i);

        i = 100;
        System.out.println("i = " + i);

        i = 300;
        System.out.println("i = " + i);

        i = 0;
        System.out.println("i = " + i);

        i = 125;
        System.out.println("i = " + i);

        System.out.println("오늘의 날씨는 10도입니다.");
        System.out.printf("오늘의 날씨는 %d도입니다.\n", 8);

        char c1 = 'a';
        char c2 = 'A';

        System.out.printf("소문자 \'%c\'의 대문자는 \'%c\'입니다.\n", c1, c2);

        double d = 1.23456d;
        System.out.printf("d = %f\n", d);
    }
}